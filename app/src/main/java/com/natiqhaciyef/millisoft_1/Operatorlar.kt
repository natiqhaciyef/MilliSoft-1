package com.natiqhaciyef.millisoft_1

fun main() {
    // +, -, *, /, %, +=,-=
    // <, >, ==, <=, >=, !=         a > b, a <= c
    // &&, ||, !                    a > b && b < c

    var b = 130
    println(b++)    // 130 + 1 = 131
    println(++b)    //  1 + 131 = 132

    var d = 5
    d/=2
    println(d)
}
